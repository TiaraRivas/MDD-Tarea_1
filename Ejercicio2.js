
function suma(a,b){
    return a+b;
}

function resta(a,b){
    return a-b;
}

function multiplicacion(a,b){
    return a*b;
}

function division(a,b){
    return a/b;
}

console.log("La suma es: ", suma(3,4));
console.log("La resta es: ", resta(10,4));
console.log("La multiplicacion es: ", multiplicacion(6,5));
console.log("La division es: ", division(81,9));